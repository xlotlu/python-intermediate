# Given the following list:
numbers = list(range(200, 150, -8))
print(numbers)
# print the length, minimum value, maximum value and sum of all elements.
print(len(numbers), min(numbers), max(numbers), sum(numbers))

# The built-in sum function can also be called on the result of the range
# function. Try it out!
print(sum(range(10, 20)))
